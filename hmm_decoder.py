    "    \n",
    "            if len(X) == 0:\n",
    "                continue\n",
    "    \n",
    "            path = viterbi_path(X, pi, A, mu, sigma)\n",
    "    \n",
    "            df_out.loc[g_sorted.index, \"viterbi_state\"] = path\n",
    "    \n",
    "        df_out[\"viterbi_state\"] = df_out[\"viterbi_state\"].astype(\"Int64\")\n",
    "        return df_out"
