    "class GaussianHMMDecoder:\n",
    "    \"\"\"\n",
    "    Gaussian Hidden Markov Model decoder using parameters\n",
    "    estimated from a Bayesian HMM (NumPyro).\n",
    "\n",
    "    Implements:\n",
    "    - emission likelihood\n",
    "    - forward algorithm\n",
    "    - viterbi decoding\n",
    "    - dataframe utilities\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, pi, A, mu, sigma):\n",
    "        \"\"\"\n",
    "        Parameters\n",
    "        ----------\n",
    "        pi : (K,)\n",
    "            Initial state probabilities\n",
    "\n",
    "        A : (K,K)\n",
    "            Transition matrix\n",
    "\n",
    "        mu : (K,D)\n",
    "            Mean of Gaussian emissions\n",
    "\n",
    "        sigma : (K,D)\n",
    "            Standard deviation (diagonal covariance)\n",
    "        \"\"\"\n",
    "\n",
    "        self.pi = np.asarray(pi)\n",
    "        self.A = np.asarray(A)\n",
    "        self.mu = np.asarray(mu)\n",
    "        self.sigma = np.asarray(sigma)\n",
    "\n",
    "        self.K = self.pi.shape[0]\n",
    "        self.D = self.mu.shape[1]\n",
    "\n",
    "        # log-space versions (used internally)\n",
    "        self.logpi = np.log(self.pi + 1e-12)\n",
    "        self.logA = np.log(self.A + 1e-12)\n",
    "    \n",
    "    # Emission Probability\n",
    "    def log_normal_diag(x, mu, sigma):\n",
    "        \"\"\"\n",
    "        x: observation sequence (T,D)\n",
    "        mu/sigma: Gaussian parameters for each time and state (K,D)\n",
    "        returns log p(x_t | z=k) as (T,K)\n",
    "        \"\"\"\n",
    "        # Expand observations so they broadcast across\n",
    "        x = x[:, None, :]               # (T,1,D)\n",
    "        mu = mu[None, :, :]             # (1,K,D)\n",
    "        sg = sigma[None, :, :]          # (1,K,D)\n",
    "\n",
    "        # Compute Gaussian log-density and sum across feature dimension D\n",
    "        return (-0.5 * (((x - mu) / sg) ** 2 + 2*np.log(sg) + np.log(2*np.pi))).sum(-1)\n",
    "\n",
    "    # Forward Algorithm\n",
    "    def forward_probs(X, pi, A, mu, sigma):\n",
    "        \"\"\"\n",
    "        X: observation sequence (T, D)\n",
    "        pi: initial state probabilities (K,)\n",
    "        A: transition matrix (K, K)\n",
    "        mu, sigma: Gaussian parameters for each state (K, D)\n",
    "        Returns gamma_filt: filtered state probabilities (T, K)\n",
    "        \"\"\"\n",
    "    \n",
    "        T = X.shape[0]\n",
    "        K = pi.shape[0]\n",
    "    \n",
    "        # Compute log emission probabilities log p(x_t | z_t=k)\n",
    "        logB = log_normal_diag(X, mu, sigma)   # (T, K)\n",
    "    \n",
    "        # Convert initial and transition probabilities to log-space\n",
    "        logpi = np.log(pi + 1e-12)             # (K,)\n",
    "        logA = np.log(A + 1e-12)               # (K, K)\n",
    "    \n",
    "        # Allocate forward probability matrix\n",
    "        log_alpha = np.zeros((T, K), dtype=np.float64)\n",
    "    \n",
    "        # t = 0\n",
    "        log_alpha[0] = logpi + logB[0]\n",
    "        log_alpha[0] -= logsumexp(log_alpha[0])   # normalize\n",
    "    \n",
    "        # t = 1, ..., T-1\n",
    "        for t in range(1, T):\n",
    "            # for each current state k:\n",
    "            # log_alpha[t, k] = logB[t, k] + logsumexp_j(log_alpha[t-1, j] + logA[j, k])\n",
    "            trans_scores = log_alpha[t - 1][:, None] + logA   # (K, K)\n",
    "            log_alpha[t] = logB[t] + logsumexp(trans_scores, axis=0)\n",
    "            log_alpha[t] -= logsumexp(log_alpha[t])           # normalize\n",
    "    \n",
    "        gamma_filt = np.exp(log_alpha)   # (T, K), rows sum to ~1\n",
    "        return gamma_filt\n",
    "    \n",
    "\n",
    "    # Viterbi Algorithm\n",
    "    def viterbi_path(X, pi, A, mu, sigma):\n",
    "        \"\"\"\n",
    "        X: observation sequence (T, D)\n",
    "        pi: initial state probabilities (K,)\n",
    "        A: transition matrix (K, K)\n",
    "        mu, sigma: Gaussian emission parameters (K, D)\n",
    "        Returns path: most likely hidden state sequence (T,)\n",
    "        \"\"\"\n",
    "    \n",
    "        T = X.shape[0]\n",
    "        K = pi.shape[0]\n",
    "    \n",
    "        # Compute emission log probabilities\n",
    "        logB = log_normal_diag(X, mu, sigma)   # (T, K)\n",
    "        logpi = np.log(pi + 1e-12)\n",
    "        logA = np.log(A + 1e-12)\n",
    "    \n",
    "        delta = np.zeros((T, K), dtype=np.float64)\n",
    "        psi = np.zeros((T, K), dtype=np.int32)\n",
    "    \n",
    "        # initialization\n",
    "        delta[0] = logpi + logB[0]\n",
    "    \n",
    "        # recursion\n",
    "        for t in range(1, T):\n",
    "            for k in range(K):\n",
    "                scores = delta[t - 1] + logA[:, k]\n",
    "                psi[t, k] = np.argmax(scores)\n",
    "                delta[t, k] = np.max(scores) + logB[t, k]\n",
    "    \n",
    "        # backtrack\n",
    "        path = np.zeros(T, dtype=np.int32)\n",
    "        path[-1] = np.argmax(delta[-1])\n",
    "    \n",
    "        for t in range(T - 2, -1, -1):\n",
    "            path[t] = psi[t + 1, path[t + 1]]\n",
    "        return path\n",
    "\n",
    "        \n",
    "    # Sequence Utilities\n",
    "    def decode_forward(self, X):\n",
    "        \"\"\"Wrapper for forward probabilities\"\"\"\n",
    "        return self.forward_probs(X)\n",
    "\n",
    "    def decode_viterbi(self, X):\n",
    "        \"\"\"Wrapper for viterbi decoding\"\"\"\n",
    "        return self.viterbi_path(X)\n",
    "\n",
    "    \n",
    "    # DataFrame Helpers\n",
    "     def add_forward_state_probs(df, feature_set, pi, A, mu, sigma, time_col=\"start_time\"):\n",
    "        \"\"\"\n",
    "        df: dataframe containing job observations\n",
    "        feature_set: columns used as HMM features\n",
    "        pi, A, mu, sigma: learned HMM parameters\n",
    "        Returns dataframe with state probability columns added\n",
    "        \"\"\"\n",
    "         \n",
    "        df_out = df.copy()\n",
    "    \n",
    "        K = len(pi)\n",
    "        # Create column names for each hidden state probability\n",
    "        prob_cols = [f\"hmm_state_{k}_prob\" for k in range(K)]\n",
    "    \n",
    "        # initialize probability columns\n",
    "        for col in prob_cols:\n",
    "            df_out[col] = np.nan\n",
    "    \n",
    "        # preserve original row identity so we can write back in correct order\n",
    "        df_out[\"_orig_idx\"] = np.arange(len(df_out))\n",
    "    \n",
    "        for (cid, iid), g in df_out.groupby([\"collection_id\", \"instance_index\"], sort=False):\n",
    "            # Process each job instance in time order\n",
    "            g_sorted = g.sort_values(time_col)\n",
    "    \n",
    "            # Extract feature sequence\n",
    "            X = g_sorted[feature_set].to_numpy(dtype=np.float32)\n",
    "    \n",
    "            if X.shape[0] == 0:\n",
    "                continue\n",
    "    \n",
    "            # Compute filtered state probabilities\n",
    "            probs = forward_probs(X, pi, A, mu, sigma)   # (T, K)\n",
    "    \n",
    "            # assign back row-by-row in sorted time order\n",
    "            df_out.loc[g_sorted.index, prob_cols] = probs\n",
    "    \n",
    "        df_out = df_out.drop(columns=\"_orig_idx\")\n",
    "        return df_out\n",
    "         \n",
    "\n",
    "    def add_viterbi_state_column(df, feature_set, pi, A, mu, sigma, time_col=\"start_time\"):\n",
    "        \"\"\"\n",
